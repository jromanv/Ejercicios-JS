//Operadores Relacionales en JS

const numero1 = 20;
const numero2 = 15;

console.log(numero1>numero2);
console.log(numero1<numero2);
console.log(numero1>=numero2);
console.log(numero1<=numero2);
console.log(numero1==numero2);

//Comparador estricto, compara el tipo de dato
console.log(numero1===numero2);

//Difeente a
console.log(numero1!=numero2);

//Comparador estricto
console.log(numero1!==numero2);

//Undefined
