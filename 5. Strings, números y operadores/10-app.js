//Operaodres Lógicos

//Operador AND, ambas preposiciones deben ser verdaderas para que la respuesta sea verdadera
const numero1 = true;
const numero2 = false;

if (numero1 && numero2){
    console.log("Verdadero");
}
else{
    console.log("Falso");
}

//Operador OR, una preposición debe ser verdadera para que la respuesta sea verdadera
if (numero1 || numero2){
    console.log("Verdadero");
}
else{
    console.log("Falso");
}




